Q.

'Zipf's law' for the probability distribution of a set of items states that
the ith most frequently occurring item occurs with probability proportional
to 1/i. That is, there is a constant c such that if K_1, ..., K_n
are the keys in order of decreasing frequency, then the frequency of
K_i is c/i. (This law is a reasonably accurate model for the frequency
of occurrence of words in the English language.) If the keys obey
Zipf's law, what is the expected number of comparisons to find a key
in a list of size n ordered by frequency?

A.

(sigma i from 1 to n i*c/i)/n = c


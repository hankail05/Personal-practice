Q.

How many directed graphs are there on a given set of n vertices?
How many undirected graphs are there on those vertices?

A.

Since directed and unirected graphs has n(n-1) and n(n-2)/2
distinct, non-loop edges, respecitvely,
 by turning on and off them we ca n get graphs
whose number is 2^(n(n-1)) and 2^(n(n-1)/2), respectively.


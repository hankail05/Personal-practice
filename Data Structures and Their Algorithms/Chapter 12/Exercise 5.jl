Q.

Prove that if a graph has a path between two vertices, it necessarily
has a simple path between the same two vertices.

A.

Suppose that there are non-simple path which the first and the last node
of the path is the same.
Then, just eliminating the path we can get a simple path.
Similarly, suppose that there are non-simple path which the last node of
the path is the previous node of the first node of it.
Assuming that a duplicated path from the last node of the non-simple path
to the first node can be compressed to a node, the same result comes.
Thus, there is always a simple path between two vertices if they produce a path.


Q.

Joe's Sorting Shoppe uses Balanced Multiway Merge with 4 tape
drives. Joe has a little capital to invest in making his Shoppe more
efficient. For $500 each he can buy more tape drives of the same
model as the ones he already has, and for $250 each he can upgrade
his existing tape drives to run 50% faster. If he has $1000 to spend,
should he buy or upgrade?

A.

The original speed is lg r / lg 2.
With two additional tapes, the speed becomes lg r / lg 3, 58% incresed speed.
With upgrading, the speed increases to 50%.
So buying new tapes is more efficient.


Q.

The proof of the Information-Theoretic Lower Bound assumes that
a comparison can have only two outcomes, < or >. But it is also
possible for the items being compared to be equal; why does this not
make any difference to the proof?

A.

Although some items can have identical value, we can give them
something distinct arbitrary and sort with given values.
So the proof is irrelevant to the presence of absense of =.


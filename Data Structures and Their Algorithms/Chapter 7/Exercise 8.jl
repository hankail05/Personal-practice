Q.

In the proof of the AVL Tree Height Theorem it is implicitly assumed
that w_h increases monotonically with h. Where is this assumption
used, and what justifies it?

A.

It is used to assure that W_h is the worst AVL tree,
and recurrence relation w_h = 1 + w_(h-1) + w_(h-2) justifies it.


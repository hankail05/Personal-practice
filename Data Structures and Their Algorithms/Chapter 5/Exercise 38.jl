Q.

Suppose t= ABCDE and p = ε, the unique string of length zero. According to the definition on page 154, does p occur as a substring of t, and if so, what should the string searching algorithms of the section return given p and t? What if t also equals ε?

A.

Because we can count a string as a set and empty set is subset of all sets, ε is a substring of t.
Even if t equals ε, there is no element to compare, so it always produces true.


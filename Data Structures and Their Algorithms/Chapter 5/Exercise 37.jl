Q.

Suppose you have several very large files to store. You may either concatenatehe files into one large file and then compress that file, or you may compress the files individually. Assuming that you are using one of the compression algorithms described in this section, does it make a difference which method you use?

A.

If the files have meaningful common parts, Lempel-Ziv algorithm can compress the integrated file more than sum of individual files.
Huffman algorithm makes no difference.

